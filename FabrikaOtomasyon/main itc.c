/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
 /* USER CODE BEGIN TIM3_IRQn 0 */
HAL_ADC_Start_IT(&hadc);
timer++;
if(timer%10==0&dur!=1){
char buffer[150];		
HAL_UART_Transmit_IT(&huart2,(uint8_t*)buffer,sprintf(buffer,"urun1=%d\n""urun2=%d\n""toplam=%d\n",urun1,urun2,toplam));
}
timer2++;
if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_11)){
timer2=1;}
if(timer2%19==0&dur != 1){
HAL_UART_Transmit_IT(&huart2,(uint8_t*)&yok,14);
}
if(sifir==2&timer%11==0&dur==1){
HAL_UART_Transmit_IT(&huart2,(uint8_t*)&hata,16);
}

/* USER CODE END TIM3_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  HAL_TIM_IRQHandler(&htim3);


  /* USER CODE BEGIN TIM3_IRQn 1 */
__HAL_UART_ENABLE_IT(&huart2,UART_IT_RXNE);
  /* USER CODE END TIM3_IRQn 1 */
}

